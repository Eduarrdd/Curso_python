# Función type para saber tipos de datos

name = "Eduardo";
print(type(name));

# Indexación: buscar posiciones de caracteres
name = 'Eduardo Rodriguez';
print(name[0]);
print(name[-1]);

# Concatenar

name = "Eduardo";
last_name = "Rodriguez"
print(name + " " + last_name);


# Función len()

name = 'Eduardo';
last_name = 'Rodriguez';
print(len(name), len(last_name));

# Métodos en Python

name = 'Eduardo';
print(name.lower());

name = 'Eduardo Rodriguez';
print(name.upper());