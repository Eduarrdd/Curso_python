# Operaciones aritméticas en Python

a = 10
b = 3

## Suma
print(f'Suma: {a} + {b}');
print(a + b);

## Resta
print(f'Resta: {a} - {b}');
print(a - b);

## Multiplicación
print(f'Multiplicación: {a} * {b}');
print(a * b);

## División
print(f'División: {a} / {b}');
print(a / b);

## Obtención de entero en división

print(f'Obtener entero: {a} // {b}');
print(a // b);

## Obtener residuo (módulo)

print(f'Obtener residuo: {a} % {b}');
print(a % b); 

## Potencia

print(f'Potencia: {a} ** {b}');
print(a ** b);

## Operadores booleanos

a = 10
b = 3

print(a > b); #Mayor que
print(a < b); #Menor que
print(a <= b); #Menor o igual que
print(a >= b); #Mayor o igual que
print(a == b); #Igual que
print(a != b); #Diferent que

