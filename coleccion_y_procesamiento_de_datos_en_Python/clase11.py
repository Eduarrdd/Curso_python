# Matrices usando listas anidadas

matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print(matriz)
print(matriz[1][2])


## Otro ejemplo de matriz con listas anidadas:

matriz2 = [
    [1, 2], [3, 4],
    [5, 6], [7, 8]
];

print(matriz2);
print(matriz2[3][0]);

